class Solution
{
    //Function to count the number of ways in which frog can reach the top.
    static long countWays(int n)
    {
        // add your code here
        long M = 1000000007;
        long dp[]=new long[n+2];
        dp[0]=1;
        dp[1]=1;
        dp[2]=2;
        for(int i=3;i<=n;i++){
            dp[i]=dp[i-1]%M+dp[i-2]%M+dp[i-3]%M;
        }
        return dp[n]%M;
    }
    
}

